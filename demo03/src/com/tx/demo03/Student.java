package com.tx.demo03;

/**
 * 学生类
 */
public class Student {
    //学号
    int id;
    //姓名
    String username;
    //性别
    String sex;
    //定义三个成绩
    //数学成绩
    int math;
    //英语
    int english;

    public Student() {
        this.id = id;
        this.username = username;
        this.sex = sex;
        this.math = math;
        this.english = english;
    }
}
